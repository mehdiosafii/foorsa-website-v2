function getMajorByName(name) {
    const key = name.toLowerCase()
        .replace(/\s+/g, '-')
        .replace(/[&\/\\#,+()$~%.'":*?<>{}–—−]/g, '')
        .replace(/--+/g, '-');
    return majorsData[key] || null;
}

function getMajorsByCategory(category) {
    return Object.values(majorsData).filter(m => m.category === category);
}

function searchMajors(query) {
    const q = query.toLowerCase();
    return Object.values(majorsData).filter(m => 
        m.name.toLowerCase().includes(q) || 
        m.description.toLowerCase().includes(q) ||
        m.category.toLowerCase().includes(q)
    );
}
